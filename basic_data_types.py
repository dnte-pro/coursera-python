"""
There are several basic data types in Python, including:
- int: Integer type, represents whole numbers.
- float: Floating-point type, represents real numbers with decimal points.
- str: String type, represents sequences of characters.
- bool: Boolean type, represents truth values (True or False).
- list: List type, represents ordered collections of items.
- tuple: Tuple type, represents ordered, immutable collections of items.
- dict: Dictionary type, represents collections of key-value pairs.
- set: Set type, represents unordered collections of unique items.
- NoneType: Represents the absence of a value (None).
"""
a = 10 #int
print(type(a))

a = 2.3 #float
print(type(a))

c = "Hello" #str
print(type(c))

d = True #bool
print(type(d))

e = [1, 2, 3] #list
print(type(e))

f = (1, 2, 3) #tuple
print(type(f))
 
g = {"name": "Alice", "age": 25} #dict
print(type(g))

h = {1, 2, 3} #set
print(type(h))

i = None #NoneType
print(type(i))

